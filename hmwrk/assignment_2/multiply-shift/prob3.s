	COVERSION OF AREA TO RADIUS SQUARED
	R1 - AREA
	R2 - CONVERSION AREA TO RADIUS - AREA * 1/PI
	R3 - RADIUS SQUARED
*/
.global _start
	_start:
		MOV R1, #0x71		@BP0  WD7
		MOV R2, #0x67 		@BP8 WD7
		ADD R3, R1,R2		@BP8 WD14
		LDR R4, =0x517D 	@BP16 WD16
		MUL R0,R3,R4		 @BP 24 WD 30
		LSR R0, #16		 @BP16  WD 6
		MOV R7, #1
		SWI 0

